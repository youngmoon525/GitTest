
public class Ex01_Operator {
	public static void main(String[] args) {
		int x = -100;
		
		int result1 = +x;//부호 +를 붙임. result1의 결과는 어떤수일까? 
		int result2 = -x;//부호 -를 붙임. 
		//결과를 꼭 1분이상 예측(생각해보고)하고 답을 적은 후에 각각의 변수들을 출력해보기.
		System.out.println("(+x) result1 = " + result1);
		System.out.println("(+x) x*1= " + x * 1);
		System.out.println("(-x) result2 = " + result2);
		System.out.println("(-x) x*-1 = " + x * -1);
		//실생활에서 앞에 +기호를 붙인걸 본적이 있는지 ? 
		//없음. - <- ※-부호는 부호를 반전 시키는 규칙이 있음. (+는 아무 규칙 없음)※
		
		
	}
}
