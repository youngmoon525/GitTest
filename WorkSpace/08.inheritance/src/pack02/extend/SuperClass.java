package pack02.extend;

public class SuperClass {
	// super();
	//1세대 : 두개의 수를 입력받아(파라메터) 합을 return하는 메소드 sum 구현해보기
	//                                              		        차를 return하는 메소드 minus 구현해보기
	public int sum(int num1 , int num2) {
		return num1 + num2 ;
	}
	public int minus(int num1 , int num2) {
		return num1 - num2;
	}
	
}
